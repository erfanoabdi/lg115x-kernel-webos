/******************************************************************************
**
**  Name: DEMOD_M14_Analog_Factory_Table_Get
**
**  Description:    Returns Factory settings table.
**
**  Parameters:     pLX_ADEMOD_Demod_Instance  - pointer to context,
**					Table		  - Factory table.
**					Size		  - Table size.
**
**  Returns:        status:
**                      retOK            - No errors
**                      retErrBadMode    - Invalid input mode.
**
******************************************************************************/
LX_ADEMOD_Result DEMOD_M14_Analog_Factory_Table_Get(pLX_ADEMOD_Demod_Instance pContext, const LX_ADEMOD_FACTORY_SETTINGS **Table, UINT32 *Size)
{
    LX_ADEMOD_Result status = retOK; /*  Status to be returned.  */

    DEMOD_PRINT( "[ABB M14](%s:%d) start !!!\n",__F__,__L__);
	DEMOD_PRINT("pContext->input_mode = 0x%x \n", pContext->input_mode);
	DEMOD_PRINT("pContext->video_std = 0x%x \n", pContext->video_std);

	switch (pContext->input_mode) {

		case LX_ADEMOD_INPUTMODE_NONE:
			if (LX_ADEMOD_DEVICE_DEBUG(pContext->device_id) & LX_ADEMOD_DEMOD_DEBUG_A2_FLAG) { // A2 flag set
				*Table = &ADEMOD_FactorySettings_InitA2;
				*Size = sizeof(ADEMOD_FactorySettings_InitA2) / sizeof(LX_ADEMOD_PARAM);
			}
			else if (LX_ADEMOD_DEVICE_DEBUG(pContext->device_id) & LX_ADEMOD_DEMOD_deBug_1_FLAG) { // deBug_1 flag set
				*Table = &ADEMOD_FactorySettings_Init_deBug_1;
				*Size = sizeof(ADEMOD_FactorySettings_Init_deBug_1) / sizeof(LX_ADEMOD_PARAM);
//			*Table = &ADEMOD_FactorySettings_Init;
//			*Size = sizeof(ADEMOD_FactorySettings_Init) / sizeof(LX_ADEMOD_PARAM);
			DEMOD_PRINT("*** ADEMOD_FactorySettings_Init_deBug_1 !!! \n");

			}
			else {
				*Table = &ADEMOD_FactorySettings_Init;
				*Size = sizeof(ADEMOD_FactorySettings_Init) / sizeof(LX_ADEMOD_PARAM);
			}
			break;

		case LX_ADEMOD_INPUTMODE_LEGACY:
			switch (pContext->video_std)
			{
				case LX_ADEMOD_VIDEO_STANDARD_NTSC_M:
				case LX_ADEMOD_VIDEO_STANDARD_NTSC_M_J:
				case LX_ADEMOD_VIDEO_STANDARD_NTSC_433:
				case LX_ADEMOD_VIDEO_STANDARD_NTSC_50:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_M:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_N:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_60:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_NC:
					*Table = &ADEMOD_FactorySettings_NTSC_MN;
					*Size = sizeof(ADEMOD_FactorySettings_NTSC_MN) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** ADEMOD_FactorySettings_NTSC_MN !!! \n");
				break;

				case LX_ADEMOD_VIDEO_STANDARD_PAL_B:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_G:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_H:
				case LX_ADEMOD_VIDEO_STANDARD_SECAM_B:
				case LX_ADEMOD_VIDEO_STANDARD_SECAM_G:
				case LX_ADEMOD_VIDEO_STANDARD_SECAM_H:
					*Table = &ADEMOD_FactorySettings_PAL_BG;
					*Size = sizeof(ADEMOD_FactorySettings_PAL_BG) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** ADEMOD_FactorySettings_PAL_BG !!! \n");
				break;

				case LX_ADEMOD_VIDEO_STANDARD_PAL_I:
					*Table = &ADEMOD_FactorySettings_PAL_I;
					*Size = sizeof(ADEMOD_FactorySettings_PAL_I) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** ADEMOD_FactorySettings_PAL_I !!! \n");
				break;

				case LX_ADEMOD_VIDEO_STANDARD_PAL_D:
				case LX_ADEMOD_VIDEO_STANDARD_PAL_K:
				case LX_ADEMOD_VIDEO_STANDARD_SECAM_D:
				case LX_ADEMOD_VIDEO_STANDARD_SECAM_K:
				case LX_ADEMOD_VIDEO_STANDARD_SECAM_K1:
					*Table = &ADEMOD_FactorySettings_PAL_DK12;
					*Size = sizeof(ADEMOD_FactorySettings_PAL_DK12) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** ADEMOD_FactorySettings_PAL_DK12 !!! \n");
				break;

				case LX_ADEMOD_VIDEO_STANDARD_PAL_DK_NICAM:
					*Table = &ADEMOD_FactorySettings_PAL_DK3;
					*Size = sizeof(ADEMOD_FactorySettings_PAL_DK3) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** ADEMOD_FactorySettings_PAL_DK3 !!! \n");
				break;

				case LX_ADEMOD_VIDEO_STANDARD_SECAM_L:
					*Table = &ADEMOD_FactorySettings_SECAM_L;
					*Size = sizeof(ADEMOD_FactorySettings_SECAM_L) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** ADEMOD_FactorySettings_SECAM_L !!! \n");
				break;

				case LX_ADEMOD_VIDEO_STANDARD_SECAM_L1:
					*Table = &ADEMOD_FactorySettings_SECAM_L1;
					*Size = sizeof(ADEMOD_FactorySettings_SECAM_L1) / sizeof(LX_ADEMOD_PARAM);
					DEMOD_PRINT("*** LX_ADEMOD_VIDEO_STANDARD_SECAM_L1 !!! \n");
				break;

				default:
					status = retErrBadParam;
				break;

			}
			break;

#if 0
		case LX_ADEMOD_INPUTMODE_DIGITAL:
			*Table = &ADEMOD_FactorySettings_LowIF;
			*Size = sizeof(ADEMOD_FactorySettings_LowIF) / sizeof(LX_ADEMOD_PARAM);
			DEMOD_PRINT("*** ADEMOD_FactorySettings_LowIF !!! \n");
			break;
#endif
		default:
			status = retErrBadMode;
			break;
	}

    DEMOD_PRINT( "[ABB M14](%s:%d) end !!!\n",__F__,__L__);

	return status;
}


